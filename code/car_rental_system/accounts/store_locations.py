# 按区获取门店
def get_stores_by_district(district):
    """根据区名获取该区的所有门店"""
    return STORE_LOCATIONS.get(district, [])

# 获取所有区名
def get_all_districts():
    """获取所有区名"""
    return list(STORE_LOCATIONS.keys())

